package aula26dejunho;

public class Triângulo_Simples {

	public static void main(String[] args) {
		
		int numeroLinha = 10;
		
		for (int i = 1; i <= numeroLinha; i++) {
			
			for (int j = 1; j <= i; j++) {
				
				System.out.print(" * ");
				
			}
			
			System.out.println("");
		}
		
	}
}
